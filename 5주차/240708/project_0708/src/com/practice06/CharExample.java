package com.practice06;

public class CharExample {

	public static void main(String[] args) {
		char var1 = 'A';
		char var2 = '가';
		char var3 = 65;
		char var4 = 44032;
		
		System.out.println(var1); // A
		System.out.println(var2); // 가
		System.out.println(var3); // A
		System.out.println(var4); // 가

	}

}
